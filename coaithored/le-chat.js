/*
	La bande dessinée la plus populaire de Philippe Geluck est sans doute "Le Chat". 
	Il a créé ce personnage en 1983 et il est devenu extrêmement populaire, 
	non seulement en Belgique, mais aussi à l'étranger fr.wikipedia.org.

	Imaginons maintenant que Philippe Geluck ait écrit "Le Chat" en utilisant JavaScript. 
	Voici comment une de ses planches pourrait se traduire en code :
*/

function LeChat() {
  // Le Chat fait une déclaration
  console.log("Je pense, donc je suis...");

  // Pause pour effet dramatique
  setTimeout(function() {
    console.log("...mais si je dors, suis-je toujours?");
  }, 3000);

  // Le Chat conclut avec sagesse
  setTimeout(function() {
    console.log("Peut-être que je suis, mais je ne suis pas sûr.");
  }, 6000);
}

// Appelons la fonction pour voir Le Chat en action
LeChat();

/*
	Dans ce script, "Le Chat" fait une déclaration ("Je pense, donc je suis..."), 
	prend une pause pour réfléchir, puis conclut avec une autre déclaration pleine de sagesse. 
	C'est une façon très simple de représenter une bande dessinée en utilisant JavaScript. 
	Bien sûr, cela ne rend pas justice à l'humour subtil et à l'art de Geluck, 
	mais c'est une interprétation ludique de son travail en code.
*/

